    private void ReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReturnActionPerformed
        
        Integer rentId = StringUtils.convertToInteger(search_field.getText());
        
        if (rentId == null) {
            JOptionPane.showMessageDialog(this, "Please Enter the data first");
            return ;
        }
        try {
            Rent rent = rentService.findById(rentId);
            
            rentService.deleteById(rent.getId());
            
            JOptionPane.showMessageDialog(this, String.format("The movie: '%s' has been returned", rent.getMovie().getName()));
            
            cleanView();
        } catch (Exception ex) {
            Logger.getLogger(ReturnHomeScreenForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_ReturnActionPerformed


    private void loadRents() {
        try {
            List<Rent> rents = rentService.listAll();
            rents.forEach(rent -> {
                Object[] row = { rent.getId(), rent.getMovie().getName(), rent.getMovie().getPrice(), rent.getCustomer().getName(), rent.getCustomer().getId() };
                DefaultTableModel model = (DefaultTableModel) table_var.getModel();
                model.addRow(row);
            });
        } catch(Exception e) {
            Logger.getLogger(RentHomeScreenForm.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    
    

    private void cleanView() {
        modelToView(null);
        search_field.setText("");
        
        DefaultTableModel model = (DefaultTableModel) table_var.getModel();
                
        int rowCount = model.getRowCount();
        
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        loadRents();
    }
    
    private void modelToView(Rent rent) {
        if (rent == null) {
            customerNameLabel.setText("");
            movieNameLabel.setText("");
            movie_price_label.setText("");
            customerIdLabel.setText("");
        } else {
            customerNameLabel.setText(rent.getCustomer().getName());
            movieNameLabel.setText(rent.getMovie().getName());
            movie_price_label.setText(StringUtils.convertToString(rent.getMovie().getPrice()));
            customerIdLabel.setText(rent.getCustomer().getId());
        }
    }

